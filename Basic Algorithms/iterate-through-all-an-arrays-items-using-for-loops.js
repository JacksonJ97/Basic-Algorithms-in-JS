function filteredArray(arr, elem) {
  let newArr = [];
  // Only change code below this line
  for (let i = 0; i < arr.length; i++) {
    for (let j = 0; j < arr[i].length; j++) {
      let check = arr[i].indexOf(elem);

      if (check === -1) {
        newArr.push(arr[i]);
        break;
      }
    }
  }
  // Only change code above this line
  return newArr;
}

